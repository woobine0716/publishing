package day02;

public class TypeCasting {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a+b);

        String c = "30";
        String d = "50";
        System.out.println(c+d);
        //int a, String c
        //애초에 타입이 다르면 +, - 같은 거 안됨
        //명시적 타입 변환
        //암묵적 타입 변환
    }
}
